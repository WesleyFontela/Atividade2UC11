    private void listarProdutosVendidos() {
        try {
            ProdutosDAO dao = new ProdutosDAO();
            DefaultTableModel model = (DefaultTableModel) tblVendas.getModel();
            model.setRowCount(0);

            ArrayList<ProdutosDTO> listagem = dao.ListarProdutosVendidos();
            for (ProdutosDTO p : listagem) {
                model.addRow(new Object[]{
                    p.getId(),
                    p.getNome(),
                    p.getValor(),
                    p.getStatus()
                });
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao carregar vendas: " + e.getMessage());
        }
    }

